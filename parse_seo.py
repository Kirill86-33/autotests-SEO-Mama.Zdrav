def get_seo_meta_tags(url):
    """Получает SEO метатеги с указанного URL и проверяет их наличие"""
    try:
        # Отправляем запрос с таймаутом 2 секунды
        response = requests.get(url, timeout=2)
        
        # Парсим HTML
        soup = BeautifulSoup(response.text, 'html.parser')
        
        # Получаем title
        title = soup.title.string.strip() if soup.title else ""
        
        # Получаем метатег description
        description_tag = soup.find('meta', attrs={'name': 'description'})
        description = description_tag['content'].strip() if description_tag and description_tag.get('content') else ""
        
        # Получаем метатег keywords
        keywords_tag = soup.find('meta', attrs={'name': 'keywords'})
        keywords = keywords_tag['content'].strip() if keywords_tag and keywords_tag.get('content') else ""
        
        # Проверяем, есть ли метатеги и не пустые ли они
        title_exists = bool(title) and title != ""
        description_exists = bool(description) and description != ""
        keywords_exists = bool(keywords) and keywords != ""
        
        return title, description, keywords, title_exists, description_exists, keywords_exists
        
    except Exception as e:
        # Если произошла ошибка
        print(f"Ошибка при обработке {url}: {str(e)}")
        return "", "", "", False, False, False

